package yingyusu_CSCI201L_Assignment1;

public class FinalJeopardy {
	private String question;
	private String answer;
	
	public FinalJeopardy (String ques, String ans) {
		this.question = ques;
		this.answer = ans;
	}
	
	public String getQuestion() {
		return question;
	}
	
	public String getAnswer() {
		return answer;
	}
}
